### STRINGS ###

# use double quotes to escape a single quote in a string
# \n  is a break line
# \t  is a tab (4 spaces)
string_a = "This's a string"
string_b = "This is a string b"

string_break = "This is a string \nbreak"
string_tab = "This is a string \tbreak"

print(string_a)
print(string_b)
print(string_break)
print(string_tab)
# len() function count the numbers of characters in a string including spaces
print(len(string_a))
