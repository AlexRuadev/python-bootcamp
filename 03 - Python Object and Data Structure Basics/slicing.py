mystring = 'abcdefghijkl'

print(mystring)

# slicing from c to the end
print(mystring[2:])

# slicing from the beginning to c
print(mystring[:3])

# slicing 'def'
print(mystring[3:6])

# slicing from the beginning to the end, with a step of 2
print(mystring[::2])

# slicing from the c to the i, with a step of 2
print(mystring[2:9:2])

# slicing the string from the end to the beginning
print(mystring[::-1])
